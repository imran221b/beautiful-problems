def addNum(x,y):
    sum = int(x) + int(y)
    return int(sum)

def numDivide(x,y):
    res = float(x) / float(y)
    return float(res)

@app.route('/problem/<id>', methods=['GET', 'POST'])
@login_required
def post(id):
    post = Post.query.filter_by(id=id).first_or_404()
    form = GiveRatingForm()

    if form.validate_on_submit():
        #post = Post.query.filter_by(problemName=problemName).first_or_404()
        post.tot = addNum(post.tot, form.addRating.data)
        post.cnt = addNum(post.cnt, int(1))
        post.rating = numDivide(post.tot, post.cnt)
        db.session.commit()

        current_user.rate(post)
        db.session.commit()

        flash('Your rating has been added!')
        return redirect(url_for('index'))

    if current_user.has_rated(post):
        return render_template('donePost.html', post=post, form=form)

    return render_template('post.html', post = post, form=form)


