@app.route('/', methods=['GET', 'POST'])
@app.route('/index', methods=['GET', 'POST'])
@login_required
def index():
    form = PostForm()

    if form.validate_on_submit():
        postsearch = Post.query.filter_by(problemName=form.problemName.data).first()
        post = Post(problemName=form.problemName.data, problemLink=form.problemLink.data, firstrating = form.tot.data, tot = form.tot.data, cnt = int(1), rating = form.tot.data, author=current_user)

        if postsearch is None:
            db.session.add(post)
            current_user.rate(post)
            db.session.commit()
            flash('Your rating has been added!')
            return redirect(url_for('index'))

        else:
            flash('You can not add this problem because it has already been added.')
            flash('You can find this problem by going to this link: https://beautiful-problem.herokuapp.com' + '/problem/' + str(postsearch.id) )
            return redirect(url_for('index'))

    page = request.args.get('page', 1, type=int)
    posts = current_user.followed_posts().paginate(
        page, app.config['POSTS_PER_PAGE'], False)
    next_url = url_for('index', page=posts.next_num) \
        if posts.has_next else None
    prev_url = url_for('index', page=posts.prev_num) \
        if posts.has_prev else None
    return render_template('index.html', title='Home', form=form,
                           posts=posts.items, next_url=next_url,
                           prev_url=prev_url)


